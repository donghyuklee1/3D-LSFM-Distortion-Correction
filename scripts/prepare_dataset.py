from __future__ import annotations

import argparse
import sys
import time
from concurrent.futures import ProcessPoolExecutor, as_completed
from pathlib import Path

import torch
import yaml
from tqdm.auto import tqdm

ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(ROOT))

from data import LSFMDistortionBeadsDataset

def _generate_one(args):
    idx, cfg, out_dir, seed = args
    out_path = Path(out_dir) / f"sample_{idx:06d}.pt"
    if out_path.exists():
        return out_path
    ds = LSFMDistortionBeadsDataset(cfg, length=idx + 1, seed=seed)
    sample = ds[idx]
    tmp_path = out_path.with_suffix(out_path.suffix + ".tmp")
    torch.save(sample, tmp_path)
    tmp_path.replace(out_path)
    return out_path

def main():
    p = argparse.ArgumentParser(description="Cache synthetic LSFM bead volumes.")
    p.add_argument("--cfg", default="configs/lsfm_beads.yaml")
    p.add_argument("--out", required=True, help="output directory")
    p.add_argument("--n", "--n_samples", dest="n", type=int, default=None,
                   help="number of volumes (default: dataset.n_samples from cfg)")
    p.add_argument("--seed", type=int, default=0)
    p.add_argument("--workers", type=int, default=0,
                   help="parallel workers (0 = serial)")
    args = p.parse_args()

    cfg = yaml.safe_load(open(args.cfg))
    n_samples = int(args.n if args.n is not None else cfg.get("dataset", {}).get("n_samples", 8192))
    out_dir = Path(args.out)
    out_dir.mkdir(parents=True, exist_ok=True)

    indices = list(range(n_samples))
    t0 = time.time()
    if args.workers <= 0:
        ds = LSFMDistortionBeadsDataset(cfg, length=n_samples, seed=args.seed)
        for i in tqdm(indices, desc="generating"):
            out_path = out_dir / f"sample_{i:06d}.pt"
            if out_path.exists():
                continue
            tmp_path = out_path.with_suffix(out_path.suffix + ".tmp")
            torch.save(ds[i], tmp_path)
            tmp_path.replace(out_path)
    else:
        jobs = [(i, cfg, str(out_dir), args.seed) for i in indices]
        with ProcessPoolExecutor(max_workers=args.workers) as ex:
            futs = [ex.submit(_generate_one, j) for j in jobs]
            for _ in tqdm(as_completed(futs), total=len(futs), desc="generating"):
                pass

    meta = {
        "n_samples": n_samples,
        "seed": args.seed,
        "dataset": "lsfm_beads",
        "cfg": cfg,
        "elapsed_sec": time.time() - t0,
    }
    torch.save(meta, out_dir / "meta.pt")
    with open(out_dir / "README.txt", "w") as f:
        f.write(
            f"Generated by scripts/prepare_dataset.py\n"
            f"n_samples = {n_samples}\n"
            f"seed      = {args.seed}\n"
            f"cfg_path  = {args.cfg}\n"
            f"elapsed   = {meta['elapsed_sec']:.1f} sec\n"
        )
    print(f"[ok] {n_samples} samples in {meta['elapsed_sec']:.1f}s -> {out_dir}")

if __name__ == "__main__":
    main()
